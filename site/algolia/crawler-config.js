// Algolia Crawler configuration for daslang.io — VERSION-CONTROLLED REFERENCE.
//
// This file is NOT consumed by any build and is NOT deployed (pages.yml does
// not copy site/algolia/). The LIVE source of truth is the Algolia dashboard:
//   Crawler → "daslang.io crawler" → Editor.
// Keep this mirror in sync when you change the dashboard config so the next
// person isn't reverse-engineering it. See README.md in this directory for the
// reasoning behind the two-action design and the RTD-specific selectors.
//
// The appId below is the public search ID (also shipped in site/files/docsearch.js).
// The crawler's admin/write API key is configured separately in the dashboard and
// is intentionally NOT part of this object.

new Crawler({
  appId: "Z88JBBPHHH",
  indexPrefix: "",
  rateLimit: 8,
  startUrls: ["https://daslang.io"],
  renderJavaScript: false,
  maxDepth: 10,
  maxUrls: null,
  schedule: "on the 23 day of the month",
  // Discovery: link-following alone never reached the deep /doc/ tree (the
  // crawl topped out at ~52 URLs). The sitemap (generated by sphinx-sitemap,
  // see doc/source/conf.py) lists all ~435 doc pages so discovery is deterministic.
  sitemaps: ["https://daslang.io/doc/sitemap.xml"],
  ignoreCanonicalTo: false,
  discoveryPatterns: ["https://daslang.io/**"],
  actions: [
    // Action 1 — Sphinx docs (RTD theme). Scoped to .rst-content so the
    // ~1635-link nav sidebar (which lives OUTSIDE .rst-content) can't leak in.
    // Each function signature <dt class="sig"> is promoted to a hierarchy LEVEL
    // (lvl4): docsearch reads it as clean text (~80-150 chars) and starts a new
    // record per function, instead of aggregating dozens of <dl><dt><dd> blocks
    // into one >97KB record. indexHeadings:true keeps signature-only functions
    // (empty <dd>) searchable. content is p/li only — never dt (the signatures
    // render as hundreds of syntax-highlight <span>s; pulling that markup into
    // content is what ballooned a 58KB-of-text page into a 349KB record).
    {
      indexName: "daslang.io crawler",
      // ast.html is handled by its own signatures-only action below (it has 546
      // symbols — too many to index with content under the 750-records cap).
      pathsToMatch: [
        "https://daslang.io/doc/**",
        "!https://daslang.io/doc/stdlib/generated/ast.html",
      ],
      recordExtractor: ({ helpers, $ }) => {
        // Drop blocks das2rst marked `.. container:: nosearch` (e.g. rtti's
        // 580-value CompilationError enum, which alone blew the per-record size
        // cap): the symbol <dt> + its description stay indexed, only the oversized
        // value/field table is removed. Toggle by symbol name in das2rst.das.
        $(".nosearch").remove();
        return helpers.docsearch({
          recordProps: {
            lvl0: {
              selectors: ".wy-breadcrumbs li.active",
              defaultValue: "Documentation",
            },
            lvl1: ".rst-content h1",
            lvl2: ".rst-content h2",
            lvl3: ".rst-content h3",
            // [id] = canonical signatures only; overloads carry no id, so they
            // don't each spawn a level record (this kept builtin under the
            // 750-records-per-page cap; ast has too many symbols even so — see
            // its dedicated signatures-only action below).
            lvl4: ".rst-content dt.sig[id]",
            lvl5: ".rst-content h4",
            lvl6: ".rst-content h5",
            // exclude the nav toctree: index pages embed the sidebar tree as
            // li.toctree-l1..l4 INSIDE .rst-content, which would otherwise be
            // counted as content and balloon the record count.
            content:
              ".rst-content p, .rst-content li:not(.toctree-l1):not(.toctree-l2):not(.toctree-l3):not(.toctree-l4)",
          },
          indexHeadings: true,
          aggregateContent: true,
          recordVersion: "v3",
        });
      },
    },
    // Action 1b — ast.html ONLY. The AST module exposes 546 public symbols;
    // even canonical-only (dt.sig[id]) that's 612 level records, and each
    // symbol's description paragraph adds a content record → 1153 > 750, which
    // fails the WHOLE page (0 records indexed). Index it signatures-only: keep
    // the dt.sig[id] hierarchy levels (search by symbol name still works) and
    // physically REMOVE the content nodes so the per-symbol descriptions don't
    // each spawn a record → 612. (A non-matching content selector does NOT work:
    // docsearch falls back to its default content selectors when the given one
    // matches nothing, re-indexing everything. Strip the DOM instead.) Action 1
    // excludes ast.html so it isn't ALSO processed there. If ast ever crosses 750
    // on signatures alone, split the page into per-group sub-pages.
    {
      indexName: "daslang.io crawler",
      pathsToMatch: ["https://daslang.io/doc/stdlib/generated/ast.html"],
      recordExtractor: ({ helpers, $ }) => {
        $(".nosearch").remove();
        // signatures-only: drop exactly what the content selector below matches.
        $(".rst-content p").remove();
        $(
          ".rst-content li:not(.toctree-l1):not(.toctree-l2):not(.toctree-l3):not(.toctree-l4)"
        ).remove();
        return helpers.docsearch({
          recordProps: {
            lvl0: {
              selectors: ".wy-breadcrumbs li.active",
              defaultValue: "Documentation",
            },
            lvl1: ".rst-content h1",
            lvl2: ".rst-content h2",
            lvl3: ".rst-content h3",
            lvl4: ".rst-content dt.sig[id]",
            lvl5: ".rst-content h4",
            lvl6: ".rst-content h5",
            content:
              ".rst-content p, .rst-content li:not(.toctree-l1):not(.toctree-l2):not(.toctree-l3):not(.toctree-l4)",
          },
          indexHeadings: true,
          aggregateContent: true,
          recordVersion: "v3",
        });
      },
    },
    // Action 2 — everything else (hand-authored Forge site + blog). The bare
    // "p, li" catch-all is needed because the landing/downloads/daspkg pages
    // have no <article>/<main>; blog posts use <article>. /doc/** is negated
    // out so doc pages aren't double-processed (a URL matching multiple actions
    // produces a record from EACH — Algolia crawler semantics).
    {
      indexName: "daslang.io crawler",
      pathsToMatch: ["https://daslang.io/**", "!https://daslang.io/doc/**"],
      recordExtractor: ({ helpers, $ }) => {
        // Harmless here (the hand-authored site/blog carry no `.nosearch`), kept
        // identical to Action 1 so the two extractors don't drift.
        $(".nosearch").remove();
        return helpers.docsearch({
          recordProps: {
            lvl0: { selectors: "", defaultValue: "Documentation" },
            lvl1: ["header h1", "article h1", "main h1", "h1", "head > title"],
            content: ["article p, article li", "main p, main li", "p, li"],
            lvl2: ["article h2", "main h2", "h2"],
            lvl3: ["article h3", "main h3", "h3"],
            lvl4: ["article h4", "main h4", "h4"],
            lvl5: ["article h5", "main h5", "h5"],
            lvl6: ["article h6", "main h6", "h6"],
          },
          aggregateContent: true,
          recordVersion: "v3",
        });
      },
    },
  ],
  safetyChecks: { beforeIndexPublishing: { maxLostRecordsPercentage: 30 } },
  initialIndexSettings: {
    "daslang.io crawler": {
      attributesForFaceting: ["type", "lang"],
      attributesToRetrieve: [
        "hierarchy",
        "content",
        "anchor",
        "url",
        "url_without_anchor",
        "type",
      ],
      attributesToHighlight: ["hierarchy", "content"],
      attributesToSnippet: ["content:10"],
      camelCaseAttributes: ["hierarchy", "content"],
      searchableAttributes: [
        "unordered(hierarchy.lvl0)",
        "unordered(hierarchy.lvl1)",
        "unordered(hierarchy.lvl2)",
        "unordered(hierarchy.lvl3)",
        "unordered(hierarchy.lvl4)",
        "unordered(hierarchy.lvl5)",
        "unordered(hierarchy.lvl6)",
        "content",
      ],
      distinct: true,
      attributeForDistinct: "url",
      customRanking: [
        "desc(weight.pageRank)",
        "desc(weight.level)",
        "asc(weight.position)",
      ],
      ranking: [
        "words",
        "filters",
        "typo",
        "attribute",
        "proximity",
        "exact",
        "custom",
      ],
      highlightPreTag: '<span class="algolia-docsearch-suggestion--highlight">',
      highlightPostTag: "</span>",
      minWordSizefor1Typo: 3,
      minWordSizefor2Typos: 7,
      allowTyposOnNumericTokens: false,
      minProximity: 1,
      ignorePlurals: true,
      advancedSyntax: true,
      attributeCriteriaComputedByMinProximity: true,
      removeWordsIfNoResults: "allOptional",
    },
  },
});
